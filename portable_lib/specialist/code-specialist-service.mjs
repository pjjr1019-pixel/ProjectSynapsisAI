import fs from "node:fs";
import path from "node:path";
import { normalizeScriptManifest } from "./manifest.mjs";
import { writeJsonFile } from "./paths.mjs";

export class CodeSpecialistService {
  constructor({ paths, codeSpecialistProvider }) {
    this.paths = paths;
    this.codeSpecialistProvider = codeSpecialistProvider;
  }

  async warm() {
    return this.codeSpecialistProvider.warm();
  }

  async propose({ request, candidates, approval = false }) {
    const proposal = await this.codeSpecialistProvider.propose({ request, candidates });
    return {
      requires_approval: true,
      approved: approval === true,
      proposal,
    };
  }

  createScriptFromProposal({ proposal, createdBy = "specialist" }) {
    fs.mkdirSync(this.paths.newSkillsRoot, { recursive: true });
    const safeName = String(proposal?.title || "new-specialist-script")
      .toLowerCase()
      .replace(/[^a-z0-9\-]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 80) || "new-specialist-script";

    const scriptRel = `brain/scripts/new-skills/${safeName}.js`;
    const scriptAbs = path.join(this.paths.taskmanagerRoot, scriptRel);
    fs.writeFileSync(scriptAbs, String(proposal?.template || "module.exports = {};\n"), "utf8");

    const { manifest } = normalizeScriptManifest(
      {
        id: safeName,
        title: proposal?.title || safeName,
        description: proposal?.reason || "Generated by specialist code fallback.",
        aliases: [],
        tags: ["generated", "specialist"],
        category: "new-skills",
        inputs: {},
        outputs: { summary: "string" },
        side_effects: [],
        permissions_needed: ["node"],
        safe_to_autorun: false,
        requires_confirmation: true,
        timeout_seconds: 30,
        platform: ["windows"],
        path: scriptRel,
        version: "1.0.0",
        created_by: createdBy,
        creation_source: "code-specialist-fallback",
        last_verified: new Date().toISOString(),
      },
      { path: scriptRel, createdBy, creationSource: "code-specialist-fallback" }
    );

    const manifestPath = path.join(this.paths.newSkillsRoot, `${safeName}.manifest.json`);
    writeJsonFile(manifestPath, manifest);

    return {
      script_path: scriptRel,
      manifest_path: path.relative(this.paths.taskmanagerRoot, manifestPath).replace(/\\/g, "/"),
      manifest,
    };
  }
}
